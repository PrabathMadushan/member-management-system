package ReportsGUI;

public class defualtReportBean {

    private String AA;
    private String SA;
    private String JHA;
    private String SN;
    private String LPN;
    private String DKA;
    private String NA;
    private String TNTR;

    public defualtReportBean(String AA, String SA, String JHA, String SN, String LPN, String DKA, String NA, String TNTR) {
        this.AA = AA;
        this.SA = SA;
        this.JHA = JHA;
        this.SN = SN;
        this.LPN = LPN;
        this.DKA = DKA;
        this.NA = NA;
        this.TNTR = TNTR;
    }

    
    
    public String getAA() {
        return AA;
    }

    public void setAA(String AA) {
        this.AA = AA;
    }

    public String getSA() {
        return SA;
    }

    public void setSA(String SA) {
        this.SA = SA;
    }

    public String getJHA() {
        return JHA;
    }

    public void setJHA(String JHA) {
        this.JHA = JHA;
    }

    public String getSN() {
        return SN;
    }

    public void setSN(String SN) {
        this.SN = SN;
    }

    public String getLPN() {
        return LPN;
    }

    public void setLPN(String LPN) {
        this.LPN = LPN;
    }

    public String getDKA() {
        return DKA;
    }

    public void setDKA(String DKA) {
        this.DKA = DKA;
    }

    public String getNA() {
        return NA;
    }

    public void setNA(String NA) {
        this.NA = NA;
    }

    public String getTNTR() {
        return TNTR;
    }

    public void setTNTR(String TNTR) {
        this.TNTR = TNTR;
    }
    

}
